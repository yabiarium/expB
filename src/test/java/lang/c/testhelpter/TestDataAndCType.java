package lang.c.testhelpter;

public class TestDataAndCType {
    private String testData;
    private int type;

    public TestDataAndCType(String testData, int type) {
        this.testData = testData;
        this.type = type;
    }

    public String getTestData() {
        return testData;
    }

    public int getType() {
        return type;
    }
}
